CREATE TABLE IF NOT EXISTS Kecamatan (
    id_kecamatan INT AUTO_INCREMENT PRIMARY KEY,
    nama VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS Jabatan (
    id_jabatan INT AUTO_INCREMENT PRIMARY KEY,
    keterangan VARCHAR(50) NOT NULL,
    gajiperjam DOUBLE NOT NULL
);

CREATE TABLE IF NOT EXISTS Kelurahan (
    id_kelurahan INT AUTO_INCREMENT PRIMARY KEY,
    nama VARCHAR(50) NOT NULL,
    id_kecamatan INT NOT NULL,
    CONSTRAINT fk_kelurahan_kecamatan FOREIGN KEY (id_kecamatan)
        REFERENCES Kecamatan (id_kecamatan)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS Pegawai (
    nohp VARCHAR(20) PRIMARY KEY,
    nama_depan VARCHAR(25) NOT NULL,
    nama_belakang VARCHAR(25),
    email VARCHAR(50),
    alamat VARCHAR(50),
    id_kelurahan INT NOT NULL,
    id_jabatan INT NOT NULL,
    statusaktif BOOLEAN NOT NULL,
    CONSTRAINT fk_pegawai_jabatan FOREIGN KEY (id_jabatan)
        REFERENCES Jabatan (id_jabatan)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT fk_pegawai_kelurahan FOREIGN KEY (id_kelurahan)
        REFERENCES Kelurahan (id_kelurahan)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS Kehadiran (
    id_kehadiran INT AUTO_INCREMENT PRIMARY KEY,
    nohp VARCHAR(20) NOT NULL,
    waktudatang DATETIME NOT NULL,
    waktupulang DATETIME,
    statusgaji BOOLEAN NOT NULL,
    CONSTRAINT fk_kehadiran_pegawai FOREIGN KEY (nohp)
        REFERENCES Pegawai (nohp)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);


